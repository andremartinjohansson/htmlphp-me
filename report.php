            jag en annan h4 och under den visas objekten som matchar sökningen.
            Om sökningen inte matchar någonting visas en sträng att sökningen
            inte matchade något. Skriptet kollar alltså om variablen med objekten
            eller artiklarna är empty innan det skrivs ut.
